saldo = 200
saque = 200

print(saldo == saque) # Saldo igual a saque
print(saldo != saque) # Saldo diferente de saque
print(saldo > saque) # Saldo maior que saque
print(saldo >= saque) # Saldo maior ou igual a saque
print(saldo < saque) # Saldo menor que saque
print(saldo <= saque) # Saldo menor ou igual a saque


