saldo = 1000
saque = 200
limite = 100
# Operador "E (and)" as duas condições precisam ser verdadeiras. Se nao retorna false
print(saldo >= saque and saque <= limite)

# Operando "OU (or)" Apenas uma condição precisa ser verdadeira.
print(saldo >= saque or saque <= limite)

